package org.javi.dd5;

public class Age {

    private int adult;
    private int lifespan;

    public Age(int adult, int lifespan) {
        this.adult = adult;
        this.lifespan = lifespan;
    }

    public int getAdult() {
        return this.adult;
    }

    public int getLifespan() {
        return this.lifespan;
    }
    
}